    private int masukkanData(Connection conn, String id_karyawan, String nama, String divisi, String domisili) throws SQLException {
        PreparedStatement pst = conn.prepareStatement("INSERT INTO Karyawan (id_karyawan, nama, divisi, domisili) VALUES(?,?,?,?)");
        pst.setString(1, id_karyawan);
        pst.setString(2, nama);
        pst.setString(3, divisi);
        pst.setString(4, domisili);
        return pst.executeUpdate();
    }
    
    private int updateData(Connection conn, String id_karyawan, String nama, String divisi, String domisili) throws SQLException {
        PreparedStatement pst = conn.prepareStatement("UPDATE karyawan set nama = ?, divisi = ?, domisili = ? WHERE id_karyawan = ?");
       
        pst.setString(1, nama);
        pst.setString(2, divisi);
        pst.setString(3, domisili);
        pst.setString(4, id_karyawan);
        return pst.executeUpdate();
    }
    
    private int hapusData(Connection conn, String id_karyawan, String nama, String divisi, String domisili) throws SQLException {
        PreparedStatement pst = conn.prepareStatement("DELETE FROM karyawan WHERE id_karyawan = ?");       
        pst.setString(1, id_karyawan);
        return pst.executeUpdate();
    }
    
    private void tampil(Connection conn) {
        dataKaryawan.clear();
        try {
            String sql = "Select * from Karyawan";
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while (rs.next()) {
                Karyawan data = new Karyawan();
                data.setId_karyawan(String.valueOf(rs.getObject(1)));
                data.setNama(String.valueOf(rs.getObject(2)));
                data.setDivisi(String.valueOf(rs.getObject(3)));
                data.setDomisili(String.valueOf(rs.getObject(4)));
                dataKaryawan.add(data);
            }
            DefaultTableModel model = (DefaultTableModel) jTableKaryawan.getModel();
            model.setRowCount(0);
            for (Karyawan data : dataKaryawan) {

                Object[] baris = new Object[4];
                baris[0] = data.getId_karyawan();
                baris[1] = data.getNama();
                baris[2] = data.getDivisi();
                baris[3] = data.getDomisili();
                model.addRow(baris);
            }
        } catch (SQLException ex) {
            Logger.getLogger(TampilanGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
