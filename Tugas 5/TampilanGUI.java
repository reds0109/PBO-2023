    private void jButtonSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSimpanActionPerformed
        // TODO add your handling code here:
        String id_karyawan = jTextFieldId.getText().trim();
        String nama = jTextFieldNama.getText();
        String divisi = jTextFieldDivisi.getText();
        String domisili = jTextFieldDomisili.getText();
        if (!id_karyawan.isEmpty() && !nama.isEmpty()) {

            try {
                // TODO add your handling code here:
                Connection conn = DriverManager.getConnection("jdbc:postgresql://localhost:5433/PBO_Pertemuan5", "postgres", "123");
                int k = masukkanData(conn, id_karyawan, nama, divisi, domisili);
                if (k > 0) {
                    tampil(conn);
                    this.peringatan("Simpan Berhasil");
                } else {
                    this.peringatan("Simpan Gagal");
                }
            } catch (SQLException ex) {
                Logger.getLogger(TampilanGUI.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            this.peringatan("Input Data Mahasiswa Gagal");
        }
        jTextFieldId.setText("");
        jTextFieldNama.setText("");
        jTextFieldDivisi.setText("");
        jTextFieldDomisili.setText("");
    }//GEN-LAST:event_jButtonSimpanActionPerformed

