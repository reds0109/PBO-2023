package pbo_uas;

/**
 *
 * @author reza danis
 */
public class SkripsiPOJO {
    private String id_skripsi;
    private String judul;
    private String pengarang;
    private String tahun;
    private String jumlah_halaman;

    /**
     * @return the id_skripsi
     */
    public String getId_skripsi() {
        return id_skripsi;
    }

    /**
     * @param id_skripsi the id_skripsi to set
     */
    public void setId_skripsi(String id_skripsi) {
        this.id_skripsi = id_skripsi;
    }

    /**
     * @return the judul
     */
    public String getJudul() {
        return judul;
    }

    /**
     * @param judul the judul to set
     */
    public void setJudul(String judul) {
        this.judul = judul;
    }

    /**
     * @return the pengarang
     */
    public String getPengarang() {
        return pengarang;
    }

    /**
     * @param pengarang the pengarang to set
     */
    public void setPengarang(String pengarang) {
        this.pengarang = pengarang;
    }

    /**
     * @return the tahun
     */
    public String getTahun() {
        return tahun;
    }

    /**
     * @param tahun the tahun to set
     */
    public void setTahun(String tahun) {
        this.tahun = tahun;
    }

    /**
     * @return the jumlah_halaman
     */
    public String getJumlah_halaman() {
        return jumlah_halaman;
    }

    /**
     * @param jumlah_halaman the jumlah_halaman to set
     */
    public void setJumlah_halaman(String jumlah_halaman) {
        this.jumlah_halaman = jumlah_halaman;
    }
}
